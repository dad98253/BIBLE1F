	PROGRAM MRGDMP
	INTEGER*2 I2A1,I2A2
	DIMENSION LENRS(4,200) , NUMLU(4)
	OPEN ( UNIT = 7 , FILE = 'MERGE.DAT' , STATUS = 'OLD' )
	I = 1
	READ ( 7 , 2 ) LENRUN , NUMRUN , NUMLU(1) ,
     +	NUMLU(2)
	WRITE ( * , 3 ) LENRUN , NUMRUN , NUMLU(1) , NUMLU(2)
    3	FORMAT ( ' LENRUN   NUMRUN    NUMLU(1)  NUMLU(2)' / 8I10 //
     +		 ' LENRS(1,I) LENRS(2,I)' )
    2	FORMAT ( 8I10 )
    1	CONTINUE
	READ ( 7 , 2 , END = 10 ) ( LENRS(J,I),J=1,2 )
	WRITE ( * , 2 ) ( LENRS(J,I),J=1,2 )
	I = I + 1
	GO TO 1
   10	CONTINUE
	CLOSE ( 7 )
C
	LIN1 = 3
	LIN2 = 4
	OPEN ( UNIT = LIN1 , FILE = 'TAPE3.DAT' , STATUS = 'UNKNOWN'
     +	     , FORM = 'UNFORMATTED' , BLOCKSIZE = 60 000 )
	OPEN ( UNIT = LIN2 , FILE = 'TAPE4.DAT' , STATUS = 'UNKNOWN'
     +	     , FORM = 'UNFORMATTED' , BLOCKSIZE = 60 000 )
C
  100	CONTINUE
C
	ICNT = 0
	DO 2000 K = 1 , 2
	    IRUN = 1
  300	    CONTINUE
	    READ ( LIN1 , END = 500 ) I2A1 , I2A2
	    ICNT = ICNT + 1
	    IF ( I2A1 .EQ. 32 000 ) THEN
		WRITE ( * , 14 ) ICNT - 1 , IRUN , LIN1
   14		FORMAT ( I10 , ' RECORDS IN RUN' , I5 , ' ON UNIT',I3)
		ICNT = 0
		IRUN = IRUN + 1
	    END IF
	    GO TO 300
  500	    CONTINUE
	    LIN1 = LIN2
 2000	CONTINUE
C
	END
