	PROGRAM LOOK
	PARAMETER ( MAXVER = 33 000 )
	PARAMETER ( MAXWRD = 25 000 )
	PARAMETER ( NDIMW  =	190 )
	PARAMETER ( NLENW  =   1024 )
	PARAMETER ( NPUN = 8 )
	COMMON /BUF/ I2IX(256) , LASTAD , IRECIN , IRMOD , I2ZERO
	INTEGER*2 I2ZERO
	INTEGER*2 I2IX
C
	INTEGER*2 I2WORD
	INTEGER*1 I1A(1 000)
	CHARACTER*23 AWORD
	CHARACTER*1000 ICA
	EQUIVALENCE ( ICA , I1A )
	I2ZERO = 0
	IRMOD = 0
	IRECIN = -1
c
C
	PRINT * , ' ENTER DATABASE NAME:'
	READ ( * , 1 , END = 1000 ) AWORD
    1	FORMAT ( A )
	OPEN ( UNIT = 10 , FILE = AWORD , STATUS = 'UNKNOWN' ,
     +	       FORM = 'UNFORMATTED' , ACCESS = 'DIRECT' , RECL = 512
     +	     , ERR = 1000 )
c
	LASTAD = 1
	CALL CHGWRD ( AWORD , 6 , LASTAD )
	PRINT * , ' VERSION = ' , AWORD(1:6)
	LASTAD = 4
	CALL I4GWRD ( LOCTOC , LASTAD )
	PRINT * , ' LOCATION OF TOC =' ,  LOCTOC
	LASTAD = 6
	CALL I4GWRD ( LOCWL , LASTAD )
	PRINT * , ' LOCATION OF WORD LIST =' ,	LOCWL
	LASTAD = 8
	CALL I4GWRD ( IADOFF , LASTAD )
	PRINT * , ' LOCATION OF INDEX =' ,  IADOFF
C
  100	CONTINUE
C
	PRINT * , ' DO YOU WISH TO?'
	PRINT * , ' 1) LOOK AT I1 DATA'
	PRINT * , ' 2) LOOK AT I2 DATA'
	PRINT * , ' 3) LOOK AT I4 DATA'
	PRINT * , ' 4) LOOK AT CHARACTER DATA'
	PRINT * , ' 5) STOP THE PROGRAM'
	READ ( * , * , END = 100 ) JUMP
	IF ( JUMP .LT. 1 .OR. JUMP .GT. 5 ) GO TO 100
	IF ( JUMP .EQ. 5 ) STOP
	PRINT * , ' ENTER THE ADDRESS'
	READ ( * , * , END = 100 ) IAD
	IF ( IAD .LE. 0 ) GO TO 100
	IF ( JUMP .EQ. 1 .OR. JUMP .EQ. 4 ) THEN
	    PRINT * , ' ENTER NUMBER OF BYTES TO DUMP:'
	    READ ( * , * , END = 100 ) LEN
	    IF ( LEN .LE. 0 .OR. LEN .GT. 1 000 ) GO TO 100
	END IF
	IF ( JUMP .EQ. 2 ) THEN
	    PRINT * , ' ENTER THE NUMBER OF I2 WORDS TO DUMP:'
	    READ ( * , * , END = 100 ) LEN
	    IF ( LEN .LE. 0 ) GO TO 100
	END IF
	IF ( JUMP .EQ. 3 ) THEN
	    PRINT * , ' ENTER THE NUMBER OF I4 WORDS TO DUMP:'
	    READ ( * , * , END = 100 ) LEN
	    IF ( LEN .LE. 0 ) GO TO 100
	END IF
	GO TO ( 101 , 102 , 103 , 104 ) , JUMP
  101	CONTINUE
	CALL I1GWRD ( I1A , LEN , IAD )
	PRINT * , ( I1A(I) , I = 1 , LEN )
	GO TO 100
  102	CONTINUE
	DO 200 I = IAD , IAD + LEN - 1
	CALL GETWRD ( I2WRD , I )
	PRINT * , I , I2WRD
  200	CONTINUE
	GO TO 100
  103	CONTINUE
	DO 300 I = IAD , IAD + LEN - 1 , 2
	CALL I4GWRD ( I4WRD , I )
	PRINT * , I , I4WRD
  300	CONTINUE
	GO TO 100
  104	CONTINUE
	CALL CHGWRD ( ICA , LEN , IAD )
	PRINT * , ICA(1:LEN)
	GO TO 100
 1000	CONTINUE
	END
